<?php

// operasi increment
$a = 10;
echo ++$a . '<br/>'; // or $a++ tergantung kondisi

// operasi decremenet
$a = 10;
echo --$a . '<br/>';

// operasi penggabungan string
$a = 'kampus';
$b = 'merdeka';
$c = $a . '-' . $b;
echo $c . '<br/>';

// operator ternary
$a = 10;
$b = $a == 10 ? 'benar' : 'salah';
echo $b . '<br/>';

?>
